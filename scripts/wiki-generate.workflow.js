function frontmatter(p) {
  const sources = p.sources.length
    ? 'sources:\n' + p.sources.map((s) => `  - ${s}`).join('\n')
    : 'sources: []'
  const related = p.related.length ? `[${p.related.join(', ')}]` : '[]'
  return [
    '---',
    `title: ${p.title}`,
    `subsystem: ${p.subsystem}`,
    sources,
    'synced_sha: pending',
    `synced: ${SYNCED}`,
    `related: ${related}`,
    '---',
  ].join('\n')
}

phase('Generate')
log(`Generating ${PAGES.length} wiki pages`)

const results = await parallel(PAGES.map((p) => () =>
  agent(
    [
      `You are writing one page of the Satie codebase wiki (a natural-language mirror of the code).`,
      `Repo root: ${ROOT}`,
      ``,
      `WRITE the file to (absolute): ${ROOT}/docs/wiki/${p.path}`,
      ``,
      `The file MUST begin with EXACTLY this frontmatter block, verbatim (do not change synced_sha):`,
      '```',
      frontmatter(p),
      '```',
      ``,
      `Then the body. First READ these source files in full and base everything on what they actually contain (use path:line references that you verify):`,
      ...p.sources.map((s) => `  - ${ROOT}/${s}`),
      p.sources.length === 0 ? `  (no source files — this is an overview/hub page; summarize and link siblings)` : ``,
      ``,
      `FOCUS: ${p.focus}`,
      ``,
      `Body section order (use these as "## " headings; omit a section only if truly N/A):`,
      `1. Purpose — one line.`,
      `2. Why it exists / responsibilities.`,
      `3. Mental model — how to think about it; a small ASCII diagram only if it earns its space.`,
      `4. Key types & functions — the public surface, each with a clickable path:line ref (e.g. src/engine/core/SatieParser.ts:412). Verify line numbers against the file you read.`,
      `5. Data flow — who calls in, what it calls out; link neighbor wiki pages by relative path (e.g. [parser](./parser.md), [hooks](../ui/hooks.md)).`,
      `6. Invariants & gotchas — non-obvious rules; cross-link docs/lessons.md entries by number where relevant.`,
      `7. Change checklist — what else to touch when this changes.`,
      `8. Sources — bullet list of the files in the frontmatter sources.`,
      ``,
      `Style: terse, concrete, for an engineer who has never opened the file. Use the codebase's own terms. NO emojis. Prefer accuracy over completeness — never invent APIs or line numbers you did not see.`,
      `After writing the file, reply with just: "wrote ${p.path}".`,
    ].filter((l) => l !== ``).join('\n'),
    { label: p.path, phase: 'Generate' },
  ).then((r) => ({ path: p.path, ok: true })).catch((e) => ({ path: p.path, ok: false, err: String(e) }))
))

const failed = results.filter((r) => r && !r.ok)
log(`Done: ${results.length - failed.length}/${PAGES.length} pages written`)
if (failed.length) log(`Failed: ${failed.map((f) => f.path).join(', ')}`)
return { written: results.length - failed.length, failed }
